const fs = require("fs");
const path = require("path");
const crypto = require("crypto");
const { Client } = require("pg");

(async () => {
  const root = process.cwd();
  const journalPath = path.join(root, "apps", "api", "drizzle", "meta", "_journal.json");
  const drizzleDir = path.join(root, "apps", "api", "drizzle");

  const journal = JSON.parse(fs.readFileSync(journalPath, "utf8"));
  const entries = journal.entries
    .filter((e) => /^000[0-4]_/.test(e.tag))
    .sort((a, b) => a.idx - b.idx);

  const rows = entries.map((entry) => {
    const sqlPath = path.join(drizzleDir, `${entry.tag}.sql`);
    const sql = fs.readFileSync(sqlPath, "utf8");
    const hash = crypto.createHash("sha256").update(sql).digest("hex");
    return {
      tag: entry.tag,
      hash,
      created_at: String(entry.when),
    };
  });

  const envPath = path.join(root, "apps", "api", ".env");
  const envText = fs.readFileSync(envPath, "utf8");
  const dbLine = envText.split(/\r?\n/).find((line) => line.startsWith("DATABASE_URL="));

  if (!dbLine) {
    throw new Error("DATABASE_URL not found in apps/api/.env");
  }

  const connectionString = dbLine.replace(/^DATABASE_URL=/, "").trim();

  const client = new Client({ connectionString });
  await client.connect();

  try {
    await client.query("begin");
    await client.query('create schema if not exists "drizzle"');
    await client.query(`
      create table if not exists "drizzle"."__drizzle_migrations" (
        id integer generated by default as identity primary key,
        hash text not null,
        created_at bigint
      )
    `);

    for (const row of rows) {
      await client.query(
        `
          insert into "drizzle"."__drizzle_migrations" (hash, created_at)
          select $1, $2
          where not exists (
            select 1
            from "drizzle"."__drizzle_migrations"
            where hash = $1
          )
        `,
        [row.hash, row.created_at]
      );
      console.log(`baselined ${row.tag}`);
    }

    await client.query("commit");
    console.log("baseline complete");
  } catch (err) {
    await client.query("rollback");
    throw err;
  } finally {
    await client.end();
  }
})();
